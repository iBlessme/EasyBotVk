a = int(input('Введите число: '))
b = int(input('Введите число: '))

print('Сумма: '        + str(a + b))
print('Разность: '     + str(a - b))
print('Произведение: ' + str(a * b))
print('Деление: '      + str(a / b))

print('Деление нацело: '     + str(a // b))
print('Остаток от деления: ' + str(a % b))
